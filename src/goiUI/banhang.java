    private void tfPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfPayActionPerformed
        // thanh toán
        tfTienNhanCuaKachActionPerformed(evt);
        if (ListGioHang.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Hãy thêm món ăn vào đơn hàng để thanh toán");
        } else if (Float.parseFloat(tfTienNhanCuaKach.getText()) < tongtien()) {
            JOptionPane.showMessageDialog(rootPane, "Số dư không đủ");
        } else {
            String tenhoadon = JOptionPane.showInputDialog(this, "Nhập tên hóa đơn đã thanh toán");
            if (tenhoadon.trim().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Xin mời nhập hóa đơn");
            } else {
                String tencacmonanString = "";
                int soluongcacmonan = 0;
                for (Monan monan : ListGioHang) {
                    tencacmonanString += monan.getTenmonString() + "(" + monan.getSoluongint() + ") ";
                    soluongcacmonan += monan.getSoluongint();
                }
                float giatong = tongtien();
                int ban = Integer.valueOf(tfSoban.getText());
                if (DAOthanhtoan.taohoadon(tenhoadon, tencacmonanString, soluongcacmonan, giatong, ban)) {
                    JOptionPane.showMessageDialog(rootPane, "Bạn đã thanh toán");
                    ListGioHang.clear();
                    showtablemonan();
                    // tra ve lai gia tri 0
                    lbTongTien.setText(String.valueOf(tongtien()));
                    tfTienNhanCuaKach.setText("0");
                    lbTienthua.setText("0");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Tên đơn hàng đã có");
                }
            }
        }
    }//GEN-LAST:event_tfPayActionPerformed

