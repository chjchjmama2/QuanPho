    void showtablemonan() {
        modelMonan.setRowCount(0);
        int i = 1;
        for (Monan monan : ListGioHang) {
            modelMonan.addRow(new Object[]{
                i++, monan.getTenmonString(), monan.getLoaitoString(), monan.getThitStrings(), monan.getSoluongint(), monan.getGiaFloat()
            });
        }

    }

    float tongtien() {
        float tongtientatca = 0;
        for (Monan monan : ListGioHang) {
            tongtientatca += monan.getGiaFloat();
        }
        return tongtientatca;
    }

