    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: search
        String searchString = jTextField1.getText();
        if (searchString.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Điền dữ liệu muốn tìm");
        } else {
            int i = 1;
            int flat = 0;
            model.setRowCount(0);
            for (Hoadon hoadon : list) {
                if (String.valueOf(hoadon.getNgaythanhtoanDate()).indexOf(searchString) > 0 ) {
                    model.addRow(new Object[]{
                        i++, hoadon.getTenhoadonString(), hoadon.getTenmonString(), hoadon.getTongsomon(), hoadon.getTongtien(), hoadon.getNgaythanhtoanDate()
                    });
                    flat = 1;
                }
            }
            if (flat == 0) {
                JOptionPane.showMessageDialog(rootPane, "Không tìm thấy kết quả nào phù hợp");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

