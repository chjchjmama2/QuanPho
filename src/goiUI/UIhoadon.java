    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: xoa
        int DeleteIndex = jTable1.getSelectedRow();
        if (DeleteIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chọn 1 hóa đơn để xóa");
        } else if (list.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Không có dữ liệu để xóa");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc là sẽ xóa không");
            if (result == JOptionPane.YES_OPTION) {
                Hoadon hd = list.get(DeleteIndex);
                if (DAOhoadon.xoaHoadon(hd)) {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    list.remove(DeleteIndex);
                    showhoadon();
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thất bại!\n Xin thử lại");
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

