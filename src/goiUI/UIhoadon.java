    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: chinh sua
        if (jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("") || jTextField6.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Không được để ô trống");
        } else {
            int selectIndex = jTable1.getSelectedRow();
            int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc với thay đổi của mình");
            if (result == JOptionPane.YES_OPTION) {
                Hoadon hd = list.get(selectIndex);
                hd.setTenhoadonString(jTextField2.getText());
                hd.setTenmonString(jTextField3.getText());
                hd.setTongsomon(Integer.parseInt(jTextField4.getText()));
                hd.setTongtien(Float.parseFloat(jTextField5.getText()));
                try {
                    hd.setNgaythanhtoanDate(new SimpleDateFormat("yyyy-MM-dd").parse(jTextField6.getText()));
                } catch (ParseException ex) {
                    System.out.println("Loi phan set ngay thanh toan");
                }
                if (DAOhoadon.suaHoadon(hd)) {
                    JOptionPane.showMessageDialog(rootPane, "Sửa thành công");

                    System.out.println(hd.getNgaythanhtoanDate());
                    showhoadon();
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
                    System.out.println(hd.getNgaythanhtoanDate());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

