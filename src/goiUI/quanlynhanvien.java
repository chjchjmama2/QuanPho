    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here: tim kiem
        String searchString = jTextField6.getText();
        if (searchString.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Điền dữ liệu muốn tìm");
        } else {
            int i = 1;
            int flat = 0;
            for (Nhanvien nv : list) {
                if (searchString.equalsIgnoreCase(nv.getHotennvString1())) {
                    tbm.setRowCount(0);
                    tbm.addRow(new Object[]{
                        i++, nv.getManvInt(), nv.getHotennvString1(), nv.getDiachiString1(), nv.getNgaysinhDate(), nv.getCmndString(), nv.getDienthoaiString1(), nv.getNgaylamviecDate(), nv.getLuongFloat()
                    });
                    flat = 1;
                } else if (searchString.equalsIgnoreCase(String.valueOf(nv.getManvInt()))) {
                    tbm.setRowCount(0);
                    tbm.addRow(new Object[]{
                        i++, nv.getManvInt(), nv.getHotennvString1(), nv.getDiachiString1(), nv.getNgaysinhDate(), nv.getCmndString(), nv.getDienthoaiString1(), nv.getNgaylamviecDate(), nv.getLuongFloat()
                    });
                    flat = 1;
                }
            }
            if (flat == 0) {
                JOptionPane.showMessageDialog(rootPane, "Không tìm thấy kết quả nào phù hợp");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

