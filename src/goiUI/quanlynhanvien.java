    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: Them nhan vien
        if (jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("") || jTextField13.getText().equals("") || jTextField14.getText().equals("") || jTextField15.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Xin mời nhập dữ liệu");
        } else {
            Nhanvien nv = new Nhanvien();
            nv.setHotennvString1(jTextField2.getText());
            nv.setDiachiString1(jTextField3.getText());
            try {
                nv.setNgaysinhDate(new SimpleDateFormat("dd/MM/yyyy").parse(jTextField4.getText()));
            } catch (ParseException ex) {
                System.out.println("Loi phan set ngay sinh");
            }
            nv.setCmndString(jTextField5.getText());
            nv.setDienthoaiString1(jTextField13.getText());
            try {
                nv.setNgaylamviecDate(new SimpleDateFormat("dd/MM/yyyy").parse(jTextField14.getText()));
            } catch (ParseException e) {
                System.out.println("Loi phan set ngay lam viec");
            }
            nv.setLuongFloat(Float.parseFloat(jTextField15.getText()));

            if (new DAOnhanvien().addNhanvien(nv)) {
                JOptionPane.showMessageDialog(rootPane, "Bạn đã thêm thành công");
                list.add(nv);
                list = new DAOnhanvien().getlistNhanviens();
                showtable();
                jLabel9.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField13.setText("");
                jTextField14.setText("");
                jTextField15.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Mã nhân viên không được trùng!!!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: Xoa nhan vien
        int removeIndex = jTable1.getSelectedRow();
        if (removeIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn một dòng rồi ấn nút xóa!");
        } else if (list.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Không có thông tin để xóa!");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc là muốn xóa nhân viên này ra khỏi danh sách ?");
            if (result == JOptionPane.YES_OPTION) {
                Nhanvien nv = list.get(removeIndex);
                DAOnhanvien.xoaNhanvien(nv);
                JOptionPane.showMessageDialog(rootPane, "Bạn đã xóa nhân viên thành công ");
                list.remove(removeIndex);
                showtable();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

